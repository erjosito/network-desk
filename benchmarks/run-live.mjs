#!/usr/bin/env node
// Tier 3 live A/B driver — COPILOT_HOME isolation version.
//
// Each variant runs in its own isolated COPILOT_HOME so the child copilot
// process doesn't conflict with the parent's network-desk extension state
// ("External tool name clash" error). Both isolated homes are pre-populated:
//   - copilot-home-ours/extensions/network-desk/    (ours + minisearch)
//   - copilot-home-upstream/extensions/network-desk/ (upstream)
//
// For each prompt, we run both variants back-to-back, alternating which
// variant goes first per prompt to counter-balance time-based confounds.
// Child copilot inherits GitHub auth from the user's credentials (Windows
// credential manager / gh CLI); only the .copilot config tree is isolated.

import { spawn } from "node:child_process";
import { readFile, writeFile, mkdir, access } from "node:fs/promises";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { performance } from "node:perf_hooks";

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO = resolve(HERE, "..");
const STATE_PATH = join(HERE, ".tier3-state.json");
const QUERIES_PATH = join(HERE, "queries-tier3.json");
const RUNS_DIR = join(HERE, "runs", "tier3", "answers");
const META_PATH = join(HERE, "runs", "tier3", "run-meta.json");

const ANSWER_MODEL = process.env.TIER3_ANSWER_MODEL || "gpt-5.2";
const PROMPT_PREFIX = "@network-desk ";
const PER_CALL_TIMEOUT_MS = 600_000; // 10 min per copilot call
const COPILOT_FLAGS = [
  "--experimental",
  "--allow-all-tools",
  "--no-ask-user",
  "--no-auto-update",
  "--model", ANSWER_MODEL,
];

let state;

async function loadState() {
  state = JSON.parse(await readFile(STATE_PATH, "utf8"));
}

async function exists(p) { try { await access(p); return true; } catch { return false; } }

function spawnCopilot({ prompt, sharePath, stdoutPath, copilotHome, timeoutMs }) {
  return new Promise((resolveP) => {
    const args = [...COPILOT_FLAGS, "--share", sharePath, "-p", prompt];
    const t0 = performance.now();
    const env = {
      ...process.env,
      COPILOT_HOME: copilotHome,
      NETWORK_DESK_NO_UPDATE_CHECK: "1",
    };
    const child = spawn("copilot", args, { cwd: REPO, env, shell: false, windowsHide: true });
    const stdoutChunks = [];
    const stderrChunks = [];
    child.stdout.on("data", (d) => stdoutChunks.push(d));
    child.stderr.on("data", (d) => stderrChunks.push(d));
    let timedOut = false;
    const timer = setTimeout(() => { timedOut = true; child.kill("SIGTERM"); }, timeoutMs);

    child.on("close", async (code, signal) => {
      clearTimeout(timer);
      const elapsedMs = performance.now() - t0;
      const stdout = Buffer.concat(stdoutChunks).toString("utf8");
      const stderr = Buffer.concat(stderrChunks).toString("utf8");
      try {
        await writeFile(stdoutPath, stdout + (stderr ? `\n--- stderr ---\n${stderr}` : ""), "utf8");
      } catch {}
      resolveP({ exitCode: code, signal, timedOut, elapsedMs: Math.round(elapsedMs), stdout, stderr });
    });

    child.on("error", async (err) => {
      clearTimeout(timer);
      const elapsedMs = performance.now() - t0;
      try { await writeFile(stdoutPath, `SPAWN_ERROR: ${err.message}`, "utf8"); } catch {}
      resolveP({ exitCode: -1, signal: null, timedOut: false, elapsedMs: Math.round(elapsedMs), spawnError: err.message });
    });
  });
}

// Extract just the final assistant answer from --share markdown (last
// "### 💬 Copilot" block, strip trailing "Generated by ..." footer).
function extractFinalAnswer(shareMd) {
  if (!shareMd) return "";
  const marker = "### 💬 Copilot";
  const idx = shareMd.lastIndexOf(marker);
  if (idx < 0) return "";
  let body = shareMd.slice(idx + marker.length).trim();
  const footerCut = body.search(/\n---\s*\n+<sub>Generated by /);
  if (footerCut > 0) body = body.slice(0, footerCut).trim();
  return body;
}

// Count cn_* tool-call mentions in the share transcript as a process metric.
function countToolCalls(shareMd) {
  if (!shareMd) return {};
  const counts = {};
  const re = /\bcn_(capabilities|route|role|orchestrate|skill|search)\b/g;
  let m;
  while ((m = re.exec(shareMd))) {
    const k = "cn_" + m[1];
    counts[k] = (counts[k] || 0) + 1;
  }
  return counts;
}

// Smoke-verify: confirm the expected tool surface is exposed in this home.
async function smokeVerify(label, copilotHome, expectedTool) {
  const sharePath = join(RUNS_DIR, `_smoke_${label}.md`);
  const stdoutPath = join(RUNS_DIR, `_smoke_${label}.txt`);
  const out = await spawnCopilot({
    prompt: "List ONLY tool names starting with 'cn_'. Comma-separated. Then exit.",
    sharePath, stdoutPath, copilotHome,
    timeoutMs: 120_000,
  });
  let answer = "";
  try { answer = extractFinalAnswer(await readFile(sharePath, "utf8")); } catch {}
  const hasExpected = answer.toLowerCase().includes(expectedTool.toLowerCase());
  console.log(`  smoke[${label}] exit=${out.exitCode} time=${out.elapsedMs}ms expected="${expectedTool}" present=${hasExpected}`);
  console.log(`    answer: ${answer.slice(0, 200).replace(/\n/g, ' ')}`);
  return { ok: hasExpected, answer, ...out };
}

async function main() {
  await loadState();
  await mkdir(RUNS_DIR, { recursive: true });
  const queries = JSON.parse(await readFile(QUERIES_PATH, "utf8")).queries;

  if (!await exists(state.homeOurs) || !await exists(state.homeUpstream)) {
    throw new Error("Isolated COPILOT_HOMEs missing — re-stage first");
  }

  const runMeta = {
    runId: state.runId,
    startedAt: new Date().toISOString(),
    answerModel: ANSWER_MODEL,
    copilotFlags: COPILOT_FLAGS,
    promptPrefix: PROMPT_PREFIX,
    timeoutMsPerCall: PER_CALL_TIMEOUT_MS,
    nodeVersion: process.version,
    repo: REPO,
    homeOurs: state.homeOurs,
    homeUpstream: state.homeUpstream,
    queries: queries.map((q) => q.id),
    results: [],
  };

  // Smoke verify each variant: OURS must expose cn_search; UPSTREAM must NOT.
  console.log("=== Smoke verification ===");
  const sOurs = await smokeVerify("ours", state.homeOurs, "cn_search");
  const sUp = await smokeVerify("upstream", state.homeUpstream, "cn_skill");
  const upstreamNoSearch = !sUp.answer.toLowerCase().includes("cn_search");
  console.log(`  upstream lacks cn_search (expected): ${upstreamNoSearch}`);
  runMeta.smoke = {
    ours: { ok: sOurs.ok, answer: sOurs.answer },
    upstream: { ok: sUp.ok, answer: sUp.answer, lacksCnSearch: upstreamNoSearch },
  };
  if (!sOurs.ok || !sUp.ok || !upstreamNoSearch) {
    console.error("⚠ Smoke verify failed — variants are not correctly isolated. Aborting.");
    runMeta.aborted = true;
    await writeFile(META_PATH, JSON.stringify(runMeta, null, 2), "utf8");
    process.exit(2);
  }

  // Per-prompt loop, interleaving variant order.
  console.log(`\n=== ${queries.length} prompts × 2 variants × answer model=${ANSWER_MODEL} ===`);
  for (let i = 0; i < queries.length; i++) {
    const q = queries[i];
    const oursFirst = i % 2 === 0;
    const order = oursFirst ? ["ours", "upstream"] : ["upstream", "ours"];
    console.log(`\n[${i + 1}/${queries.length}] ${q.id}  (order: ${order.join(", ")})`);
    console.log(`    Q: ${q.query}`);

    const result = { id: q.id, query: q.query, category: q.category, order, variants: {} };

    for (const variant of order) {
      const home = variant === "ours" ? state.homeOurs : state.homeUpstream;
      const sharePath = join(RUNS_DIR, `${q.id}-${variant}.md`);
      const stdoutPath = join(RUNS_DIR, `${q.id}-${variant}.txt`);
      const r = await spawnCopilot({
        prompt: PROMPT_PREFIX + q.query,
        sharePath, stdoutPath, copilotHome: home,
        timeoutMs: PER_CALL_TIMEOUT_MS,
      });
      let shareMd = "";
      try { shareMd = await readFile(sharePath, "utf8"); } catch {}
      const finalAnswer = extractFinalAnswer(shareMd);
      const toolCounts = countToolCalls(shareMd);
      const tokensMatch = r.stdout?.match(/↑\s*([\d.]+k?)\s*(?:\(([\d.]+k?)\s*cached\))?[^↓]*↓\s*([\d.]+k?|\d+)/s);
      const creditsMatch = r.stdout?.match(/AI Credits\s+([\d.]+)/);
      result.variants[variant] = {
        exitCode: r.exitCode,
        timedOut: r.timedOut,
        elapsedMs: r.elapsedMs,
        sharePath: sharePath.replace(REPO + "\\", "").replace(/\\/g, "/"),
        stdoutPath: stdoutPath.replace(REPO + "\\", "").replace(/\\/g, "/"),
        finalAnswerLen: finalAnswer.length,
        toolCounts,
        tokens: tokensMatch ? { up: tokensMatch[1], cached: tokensMatch[2] ?? null, down: tokensMatch[3] } : null,
        credits: creditsMatch ? parseFloat(creditsMatch[1]) : null,
      };
      console.log(`    ${variant.padEnd(8)} exit=${r.exitCode} time=${(r.elapsedMs / 1000).toFixed(1)}s answer=${finalAnswer.length}B tools=${Object.keys(toolCounts).length} credits=${result.variants[variant].credits ?? "?"}`);
    }
    runMeta.results.push(result);
    await writeFile(META_PATH, JSON.stringify(runMeta, null, 2), "utf8");
  }

  runMeta.finishedAt = new Date().toISOString();
  await writeFile(META_PATH, JSON.stringify(runMeta, null, 2), "utf8");
  console.log(`\n✓ Done. Meta: ${META_PATH}`);
}

main().catch((e) => { console.error("[driver] fatal:", e); process.exit(1); });
