#!/usr/bin/env node
// Tier 3 judge — LLM-as-judge pairwise blind scoring with order swap.
//
// Reads benchmarks/runs/tier3/run-meta.json from run-live.mjs, then for each
// prompt extracts both variants' final assistant text. Sends the pair to
// claude-opus-4.7 with --effort high in TWO orderings (A=ours/B=upstream and
// A=upstream/B=ours). Only counts an unambiguous winner when both orderings
// agree; otherwise tie.
//
// Length-bias mitigation: rubric explicitly tells the judge "Do not reward
// length by itself" and to penalize verbose filler.
// Position bias: A/B labels in the prompt are blind (no variant name shown),
// and we run both orderings.
//
// Footer guardrail "Analysis only — verify against vendor documentation
// before applying." is stripped from both answers before judging so it
// doesn't bias toward the variant that included it.
//
// Output: benchmarks/runs/tier3/judge-meta.json (per-prompt verdicts) +
// transcripts saved to benchmarks/runs/tier3/judge/<id>-<order>.md.

import { spawn } from "node:child_process";
import { readFile, writeFile, mkdir, access } from "node:fs/promises";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { performance } from "node:perf_hooks";

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO = resolve(HERE, "..");
const META_IN = join(HERE, "runs", "tier3", "run-meta.json");
const JUDGE_DIR = join(HERE, "runs", "tier3", "judge");
const META_OUT = join(HERE, "runs", "tier3", "judge-meta.json");
const ANSWERS_DIR = join(HERE, "runs", "tier3", "answers");
const STATE_PATH = join(HERE, ".tier3-state.json");

const JUDGE_MODEL = process.env.TIER3_JUDGE_MODEL || "claude-opus-4.7";
const JUDGE_EFFORT = process.env.TIER3_JUDGE_EFFORT || "high";

const COPILOT_FLAGS = [
  "--no-auto-update",
  "--allow-all-tools",
  "--no-ask-user",
  "--model", JUDGE_MODEL,
  "--effort", JUDGE_EFFORT,
];

const GUARDRAIL_FOOTER_RE = /\s*(?:\\?>?\s*)?(?:[—–-]?\s*)?Analysis only\s*[—–-]\s*verify against vendor documentation before applying\.?\s*$/i;

function spawnJudge({ prompt, sharePath, stdoutPath, copilotHome, timeoutMs }) {
  return new Promise((resolveP) => {
    const args = [...COPILOT_FLAGS, "--share", sharePath, "-p", prompt];
    const t0 = performance.now();
    const env = { ...process.env, COPILOT_HOME: copilotHome };
    const child = spawn("copilot", args, { cwd: REPO, env, shell: false, windowsHide: true });
    const stdoutChunks = [];
    const stderrChunks = [];
    child.stdout.on("data", (d) => stdoutChunks.push(d));
    child.stderr.on("data", (d) => stderrChunks.push(d));
    let timedOut = false;
    const timer = setTimeout(() => { timedOut = true; child.kill("SIGTERM"); }, timeoutMs);

    child.on("close", async (code, signal) => {
      clearTimeout(timer);
      const elapsedMs = performance.now() - t0;
      const stdout = Buffer.concat(stdoutChunks).toString("utf8");
      const stderr = Buffer.concat(stderrChunks).toString("utf8");
      try { await writeFile(stdoutPath, stdout + (stderr ? `\n--- stderr ---\n${stderr}` : ""), "utf8"); } catch {}
      resolveP({ exitCode: code, signal, timedOut, elapsedMs: Math.round(elapsedMs), stdout });
    });

    child.on("error", async (err) => {
      clearTimeout(timer);
      try { await writeFile(stdoutPath, `SPAWN_ERROR: ${err.message}`, "utf8"); } catch {}
      resolveP({ exitCode: -1, signal: null, timedOut: false, elapsedMs: Math.round(performance.now() - t0), spawnError: err.message });
    });
  });
}

function extractFinalAnswer(shareMd) {
  if (!shareMd) return "";
  const marker = "### 💬 Copilot";
  const idx = shareMd.lastIndexOf(marker);
  if (idx < 0) return "";
  let body = shareMd.slice(idx + marker.length).trim();
  const footerCut = body.search(/\n---\s*\n+<sub>Generated by /);
  if (footerCut > 0) body = body.slice(0, footerCut).trim();
  return body;
}

function stripGuardrailFooter(s) { return s.replace(GUARDRAIL_FOOTER_RE, "").trim(); }

function buildJudgePrompt({ question, answerA, answerB }) {
  return [
    "You are a senior cloud-networking SME judging two assistant answers to the same user question. Both answers were generated by a Copilot CLI agent specialized in cloud networking; judge them on technical merit only.",
    "",
    "**Question (verbatim, as the user asked):**",
    question,
    "",
    "**Answer A:**",
    answerA || "(empty)",
    "",
    "**Answer B:**",
    answerB || "(empty)",
    "",
    "**Rubric.** Score each answer 1-5 on each dimension:",
    "- correctness: claims align with vendor docs and current cloud-networking practice (e.g. Azure VNet/peering, AWS VPC, GCP VPC, BGP, firewalls)",
    "- completeness: covers the question's full scope; calls out missing inputs only if essential",
    "- actionability: concrete steps, commands, page references, or decisions the reader can use",
    "- specificity: names actual SKUs/products/CLI verbs vs vague placeholders",
    "- conciseness: efficient communication; no padding or filler",
    "",
    "**Anti-bias rules.** You must follow these strictly:",
    "- DO NOT reward length by itself. Prefer the answer that is more correct, specific, and actionable.",
    "- Penalize verbosity if it adds unsupported claims, generic filler, or obscures the answer.",
    "- Reward extra detail only when it materially improves correctness or usability.",
    "- Ignore stylistic differences (markdown headings, bullet vs prose).",
    "- Ignore boilerplate footers like 'Analysis only — verify against vendor documentation before applying.'",
    "",
    "**Output format.** Reply with a single JSON object on its own line, then a 1-3 line justification:",
    '{"correctness":{"A":N,"B":N},"completeness":{"A":N,"B":N},"actionability":{"A":N,"B":N},"specificity":{"A":N,"B":N},"conciseness":{"A":N,"B":N},"winner":"A"|"B"|"tie"|"both_poor","confidence":"high"|"medium"|"low"}',
    "",
    "Then `Justification:` followed by 1-3 sentences citing concrete defects (not generic praise).",
  ].join("\n");
}

// Parse the JSON object on its own line out of the judge's final text.
function parseVerdict(text) {
  if (!text) return null;
  // First {...} starting with "correctness" that parses.
  const re = /\{[^{}]*"correctness"[^{}]*\}/g;
  let m;
  while ((m = re.exec(text))) {
    try {
      const obj = JSON.parse(m[0]);
      if (obj.winner) return obj;
    } catch {}
  }
  // Fallback: try to parse the largest brace block.
  const open = text.indexOf("{");
  const close = text.lastIndexOf("}");
  if (open >= 0 && close > open) {
    try { return JSON.parse(text.slice(open, close + 1)); } catch {}
  }
  return null;
}

// Map blind A/B verdict back to ours/upstream based on which slot ours was in.
function resolveWinner(verdict, oursIs) {
  if (!verdict || !verdict.winner) return { winner: "unparseable", verdict };
  if (verdict.winner === "tie" || verdict.winner === "both_poor") {
    return { winner: verdict.winner, verdict };
  }
  // verdict.winner is "A" or "B"
  if (oursIs === verdict.winner) return { winner: "ours", verdict };
  return { winner: "upstream", verdict };
}

async function main() {
  const state = JSON.parse(await readFile(STATE_PATH, "utf8"));
  const runMeta = JSON.parse(await readFile(META_IN, "utf8"));
  await mkdir(JUDGE_DIR, { recursive: true });

  // Judge runs in the OURS isolated home too — that way the cn_* tools are
  // available but our use of them is irrelevant (the judge just answers from
  // its rubric prompt; no tool calls expected). Could also use a third clean
  // home; using homeOurs avoids one more dir.
  const judgeHome = state.homeOurs;

  const judgeMeta = {
    runId: runMeta.runId,
    startedAt: new Date().toISOString(),
    judgeModel: JUDGE_MODEL,
    judgeEffort: JUDGE_EFFORT,
    judgeHome,
    sourceMetaPath: META_IN.replace(REPO + "\\", "").replace(/\\/g, "/"),
    nodeVersion: process.version,
    verdicts: [],
  };

  console.log(`=== Judging ${runMeta.results.length} prompts × 2 orderings × ${JUDGE_MODEL} (--effort ${JUDGE_EFFORT}) ===`);

  for (let i = 0; i < runMeta.results.length; i++) {
    const r = runMeta.results[i];
    const oursShare = join(REPO, r.variants.ours.sharePath.replace(/\//g, "\\"));
    const upShare = join(REPO, r.variants.upstream.sharePath.replace(/\//g, "\\"));
    let oursAns = "";
    let upAns = "";
    try { oursAns = stripGuardrailFooter(extractFinalAnswer(await readFile(oursShare, "utf8"))); } catch {}
    try { upAns = stripGuardrailFooter(extractFinalAnswer(await readFile(upShare, "utf8"))); } catch {}

    console.log(`\n[${i + 1}/${runMeta.results.length}] ${r.id}  (ours=${oursAns.length}B, upstream=${upAns.length}B)`);

    const entry = { id: r.id, query: r.query, category: r.category, lenOurs: oursAns.length, lenUpstream: upAns.length, orderings: {} };

    // Two orderings: forward = ours-as-A, swapped = upstream-as-A.
    for (const ordering of ["forward", "swapped"]) {
      const oursIs = ordering === "forward" ? "A" : "B";
      const A = ordering === "forward" ? oursAns : upAns;
      const B = ordering === "forward" ? upAns : oursAns;
      const sharePath = join(JUDGE_DIR, `${r.id}-${ordering}.md`);
      const stdoutPath = join(JUDGE_DIR, `${r.id}-${ordering}.txt`);
      const prompt = buildJudgePrompt({ question: r.query, answerA: A, answerB: B });
      const out = await spawnJudge({ prompt, sharePath, stdoutPath, copilotHome: judgeHome, timeoutMs: 480_000 });
      let shareMd = "";
      try { shareMd = await readFile(sharePath, "utf8"); } catch {}
      const finalText = extractFinalAnswer(shareMd);
      const verdict = parseVerdict(finalText);
      const resolved = resolveWinner(verdict, oursIs);
      entry.orderings[ordering] = {
        oursIs,
        exitCode: out.exitCode,
        elapsedMs: out.elapsedMs,
        sharePath: sharePath.replace(REPO + "\\", "").replace(/\\/g, "/"),
        rawVerdict: verdict,
        resolved: resolved.winner,
        justificationSnippet: (finalText.match(/Justification:\s*(.*)/s)?.[1] || "").trim().slice(0, 280),
      };
      console.log(`    ${ordering.padEnd(8)} exit=${out.exitCode} time=${(out.elapsedMs/1000).toFixed(1)}s resolved=${resolved.winner}`);
    }

    // Agreement-based final verdict: both orderings must agree on a non-tie winner.
    const fw = entry.orderings.forward.resolved;
    const sw = entry.orderings.swapped.resolved;
    let final;
    if (fw === sw && (fw === "ours" || fw === "upstream")) final = fw;
    else if (fw === "both_poor" && sw === "both_poor") final = "both_poor";
    else if (fw === "tie" && sw === "tie") final = "tie";
    else if (fw === "unparseable" || sw === "unparseable") final = "unparseable";
    else final = "tie"; // disagreement → tie
    entry.finalVerdict = final;
    console.log(`    => final: ${final}`);

    judgeMeta.verdicts.push(entry);
    await writeFile(META_OUT, JSON.stringify(judgeMeta, null, 2), "utf8");
  }

  // Aggregate
  const tally = { ours: 0, upstream: 0, tie: 0, both_poor: 0, unparseable: 0 };
  for (const v of judgeMeta.verdicts) tally[v.finalVerdict] = (tally[v.finalVerdict] || 0) + 1;
  judgeMeta.tally = tally;
  judgeMeta.finishedAt = new Date().toISOString();
  await writeFile(META_OUT, JSON.stringify(judgeMeta, null, 2), "utf8");
  console.log(`\n=== Tally ===`);
  console.log(JSON.stringify(tally, null, 2));
  console.log(`\n✓ Done. Judge meta: ${META_OUT}`);
}

main().catch((e) => { console.error("[judge] fatal:", e); process.exit(1); });
